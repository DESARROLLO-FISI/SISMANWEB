package pe.edu.sistemas.sismanweb.Util;

public class Search {

		String filtro;
		String valor;
		
		
		public Search(){};
		
		
		public String getFiltro() {
			return filtro;
		}
		public void setFiltro(String filtro) {
			this.filtro = filtro;
		}
		public String getValor() {
			return valor;
		}
		public void setValor(String valor) {
			this.valor = valor;
		}
		
		
}
